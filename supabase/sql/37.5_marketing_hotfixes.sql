-- supabase/sql/37.5_marketing_hotfixes.sql
-- Marketing attribution security hardening & schema synchronization hotfixes

-- ============================================================================
-- بخش اول: امنیت و سطح دسترسی ویوی آتریبیوشن
-- ============================================================================
-- پیکربندی ویژگی security_invoker بر روی ویوی first-touch جهت اعمال سیاست‌های RLS
-- و رفع خطای امنیتی بحرانی Supabase (Critical Security Warning).
-- با این کار، پرامیشن‌ها و قوانین RLS کاربر کوئری‌زننده به جای خالق ویو اعمال می‌گردند.
ALTER VIEW marketing.v_first_touch_attribution SET (security_invoker = true);

-- ============================================================================
-- بخش دوم: مستندسازی و راهنمای به‌روزرسانی جدول محلی/تحلیلی
-- ============================================================================
-- برای دیتابیس تحلیلی خارجی (Remote Analytical DB)، ستون target_url باید روی جدول
-- فیزیکی campaigns با ساختار زیر تعریف شده باشد:
-- ALTER TABLE public.campaigns ADD COLUMN IF NOT EXISTS target_url TEXT;

-- ============================================================================
-- بخش سوم: افزودن ستون به جدول خارجی (Foreign Table)
-- ============================================================================
-- سینک ساختار شمای FDW برای جدول campaigns_fdw جهت برقراری ارتباط با فیلد هدایت
-- و رفع خطای عدم وجود ستون در کش اسکیما (Could not find the 'target_url' column).
ALTER FOREIGN TABLE marketing.campaigns_fdw ADD COLUMN IF NOT EXISTS target_url TEXT;

-- ============================================================================
-- بخش چهارم: پاکسازی کش اسکیما در PostgREST
-- ============================================================================
-- ارسال سیگنال رفرش به PostgREST برای بازخوانی فوری ساختار بهینه شناسه کش و برطرف شدن خطا
NOTIFY pgrst, 'reload schema';
